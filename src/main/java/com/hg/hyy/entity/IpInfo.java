package com.hg.hyy.entity;

public class IpInfo {

    private String Ip;
    private String UA;
    private String City;

    public IpInfo() {

    }

    public String getIp() {
        return Ip;
    }

    public void setIp(String ip) {
        Ip = ip;
    }

    public String getUA() {
        return UA;
    }

    public void setUA(String uA) {
        UA = uA;
    }

    public String getCity() {
        return City;
    }

    public void setCity(String city) {
        City = city;
    }

}
